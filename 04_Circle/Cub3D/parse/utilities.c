#include "../cub3D.h"

int	jump_sp(char *str)
{
	int	i;

	if (str == NULL)
		return (0);
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32 || str[i] != '\0')
		++i;
	if (str[i] == '\0')
		return (0);
	return (i);
}
