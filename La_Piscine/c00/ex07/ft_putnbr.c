#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	nbr[10];

	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	while (nb > 0)
	{
		nbr[i] = nb % 10 + '0';
		nb = nb / 10;
		i++;
	}
	i = i - 1;
	while (i >= 0)
	{
		write(1, &nbr[i], 1);
		i--;
	}
}
